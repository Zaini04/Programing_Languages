#include<iostream>//preprocessor directive
using namespace std;//header file
int main ()//main fuction
{//opening brace
	int a=10;//it declares value of a
	cout<<a;//it print the value of a
	a+=10;//it increase the value by 10
	cout<<"\n";//out put is show on new line
	cout<<a;//it print the value of a again
	a--;//it decrease the value of a by 1
	cout<<"\n";//output is show on new line
	cout<<a;//it print the value again
	return 0;
	}//closing brace
